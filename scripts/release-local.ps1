function Resolve-NextPatchVersion {
  $currentVersion = (node -p "require('./package.json').version").Trim()
  $parts = $currentVersion.Split(".")

  if ($parts.Count -ne 3) {
    throw "Cannot bump non-standard version: $currentVersion"
  }

  $major = 0
  $minor = 0
  $patch = 0

  if (
    -not [int]::TryParse($parts[0], [ref]$major) -or
    -not [int]::TryParse($parts[1], [ref]$minor) -or
    -not [int]::TryParse($parts[2], [ref]$patch)
  ) {
    throw "Cannot bump non-numeric version: $currentVersion"
  }

  return "$major.$minor.$($patch + 1)"
}

$repoRoot = (& git rev-parse --show-toplevel).Trim()
if ($LASTEXITCODE -ne 0 -or [string]::IsNullOrWhiteSpace($repoRoot)) {
  throw "This script must be run inside a git repository."
}
Set-Location $repoRoot

$currentBranch = (& git rev-parse --abbrev-ref HEAD).Trim()
if ($currentBranch -ne $Branch) {
  throw "Release must run on '$Branch'. Current branch: '$currentBranch'."
}

Import-DotEnvLocal
if ([string]::IsNullOrWhiteSpace($env:GOOGLE_DRIVE_REQUIRED_SCOPES)) {
  $env:GOOGLE_DRIVE_REQUIRED_SCOPES = "https://www.googleapis.com/auth/drive.file"
}

if (-not $SkipInstall) {
  Invoke-Step "Install deps" {
    Invoke-Native npm ci
  }
}

if (-not $AllowDirty) {
  $dirtyPaths = @(Get-DirtyPaths)
  $allowedResumePaths = @("package.json", "package-lock.json", "README.md")
  $unexpectedDirtyPaths = @()

  if ($UseCurrentVersion) {
    $unexpectedDirtyPaths = @(
      $dirtyPaths | Where-Object { $allowedResumePaths -notcontains $_ }
    )
  } else {
    $unexpectedDirtyPaths = @($dirtyPaths)
  }

  if ($unexpectedDirtyPaths.Count -gt 0) {
    throw "Working tree is not clean. Commit or stash changes first, or pass -AllowDirty. Dirty paths: $($unexpectedDirtyPaths -join ', ')"
  }
}

Invoke-Step "Resolve release version" {
  git config user.name "github-actions[bot]"
  git config user.email "41898282+github-actions[bot]@users.noreply.github.com"

  $script:Version =
    if ($UseCurrentVersion) {
      (node -p "require('./package.json').version").Trim()
    } else {
      Resolve-NextPatchVersion
    }
  $script:Tag = "v$script:Version"
  Write-Host $script:Tag
}

Invoke-Step "Validate Google OAuth config" {
  if ([string]::IsNullOrWhiteSpace($env:GOOGLE_OAUTH_CLIENT_ID)) {
    throw "Missing GOOGLE_OAUTH_CLIENT_ID in environment or .env.local."
  }
}

Invoke-Step "Build app (webpack -> release/app)" {
  Invoke-Native npm run build
}

if (-not $SkipCodeSigningValidation) {
  Invoke-Step "Validate PFX password" {
    if ([string]::IsNullOrWhiteSpace($env:CSC_LINK) -or [string]::IsNullOrWhiteSpace($env:CSC_KEY_PASSWORD)) {
      throw "Missing CSC_LINK or CSC_KEY_PASSWORD in environment."
    }

    $tempDir = Join-Path ([System.IO.Path]::GetTempPath()) "apprenticeship-reports-release"
    New-Item -ItemType Directory -Path $tempDir -Force | Out-Null
    $pfx = Join-Path $tempDir "cs.pfx"
    $bytes = [Convert]::FromBase64String($env:CSC_LINK)
    [IO.File]::WriteAllBytes($pfx, $bytes)
    Invoke-Native certutil -p $env:CSC_KEY_PASSWORD -dump $pfx
  }
}

Invoke-Step "Package installer" {
  Write-Host "Packaging with version $script:Version"
  $env:CSC_LINK = $env:CSC_LINK
  $env:CSC_KEY_PASSWORD = $env:CSC_KEY_PASSWORD
  Invoke-Native npx electron-builder --win nsis --publish never "--config.extraMetadata.version=$script:Version"
}

if (-not $UseCurrentVersion) {
  Invoke-Step "Apply release version to package files" {
    Invoke-Native npm version $script:Version --no-git-tag-version
  }
}

$repository = Resolve-GitHubRepository
$download = "https://github.com/$($repository.Owner)/$($repository.Repo)/releases/latest/download/Apprenticeship-Reports-Setup.exe"

Invoke-Step "Update README.md with latest version and dates" {
  $dateDE = (Get-Date).ToString("dd.MM.yyyy")
  $content = Get-Content -Path README.md -Raw
  $content = Replace-ReadmeValue $content '\[Download for Windows\]\([^)]+\)' { param($match) "[Download for Windows]($download)" } "download link"
  $content = Replace-ReadmeValue $content '(- Version:\s*)`[^`]+`' { param($match) "$($match.Groups[1].Value)``$script:Tag``" } "version"
  $content = Replace-ReadmeValue $content '(- Ver[^\r\n]* am:\s*)`[^`]+`' { param($match) "$($match.Groups[1].Value)``$dateDE``" } "publish date"
  Set-Content -Path README.md -Value $content -Encoding UTF8 -NoNewline
}

Invoke-Step "Commit bumped version and create tag" {
  & git diff --quiet
  if ($LASTEXITCODE -ne 0) {
    Invoke-Native git add package.json package-lock.json README.md
    Invoke-Native git commit -m "ci(release): $script:Tag [skip ci]"
  }

  & git rev-parse -q --verify "refs/tags/$script:Tag" *> $null
  if ($LASTEXITCODE -eq 0) {
    throw "Tag already exists locally: $script:Tag"
  }

  Invoke-Native git tag $script:Tag
}

if ($NoPublish) {
  Write-Host ""
  Write-Host "Built local release without publishing."
  Write-Host "Tag created locally: $script:Tag"
  Write-Host "Assets: release/build"
  exit 0
}

Invoke-Step "Push release commit and tag" {
  Invoke-Native git push origin "HEAD:$Branch" $script:Tag
}

Invoke-Step "Create GitHub Release" {
  Invoke-Native gh auth status

  $bodyPath = Join-Path ([System.IO.Path]::GetTempPath()) "apprenticeship-release-body.md"
  Set-Content -Path $bodyPath -Value (New-ReleaseBody -DownloadUrl $download) -Encoding UTF8

  $assets = @(
    "release/build/Apprenticeship-Reports-Setup.exe",
    "release/build/latest.yml"
  )
  $blockmaps = @(Get-ChildItem -Path "release/build" -Filter "*.blockmap" -File)
  if ($blockmaps.Count -eq 0) {
    throw "Missing release asset: *.blockmap"
  }
  $assets += @($blockmaps | ForEach-Object { $_.FullName })

  foreach ($asset in $assets) {
    if (-not (Test-Path $asset)) {
      throw "Missing release asset: $asset"
    }
  }

  $retryCount = 5
  $success = $false

  for ($i = 1; $i -le $retryCount; $i++) {
    & gh release view $script:Tag *> $null
    if ($LASTEXITCODE -eq 0) {
      & gh release upload $script:Tag @assets --clobber
      if ($LASTEXITCODE -eq 0) {
        & gh release edit $script:Tag --title $script:Tag --notes-file $bodyPath --latest
        if ($LASTEXITCODE -eq 0) {
          $success = $true
          break
        }
      }
    } else {
      & gh release create $script:Tag @assets --title $script:Tag --notes-file $bodyPath --latest
      if ($LASTEXITCODE -eq 0) {
        $success = $true
        break
      }
    }

    Write-Host "GitHub CLI error. Retrying in 5 seconds ($i/$retryCount)..."
    Start-Sleep -Seconds 5
  }

  if (-not $success) {
    throw "GitHub Release could not be created after $retryCount attempts."
  }
}

Write-Host ""
Write-Host "Release published: $script:Tag"
