param(
  [switch]$NoPublish,
  [switch]$SkipInstall,
  [switch]$SkipCodeSigningValidation,
  [switch]$AllowDirty,
  [switch]$UseCurrentVersion,
  [string]$Branch = "main"
)

$ErrorActionPreference = "Stop"
Set-StrictMode -Version Latest

function Invoke-Native {
  param(
    [Parameter(Mandatory = $true)][string]$FilePath,
    [Parameter(ValueFromRemainingArguments = $true)][string[]]$Arguments
  )

  & $FilePath @Arguments
  if ($LASTEXITCODE -ne 0) {
    throw "$FilePath $($Arguments -join ' ') failed with exit code $LASTEXITCODE."
  }
}

function Invoke-Step {
  param(
    [Parameter(Mandatory = $true)][string]$Name,
    [Parameter(Mandatory = $true)][scriptblock]$ScriptBlock
  )

  Write-Host ""
  Write-Host "==> $Name"
  & $ScriptBlock
}

function Import-DotEnvLocal {
  $envPath = Join-Path (Get-Location) ".env.local"
  if (-not (Test-Path $envPath)) {
    return
  }

  Get-Content $envPath | ForEach-Object {
    $line = $_.Trim()
    if (-not $line -or $line.StartsWith("#") -or -not $line.Contains("=")) {
      return
    }

    $key, $value = $line.Split("=", 2)
    $key = $key.Trim()
    $value = $value.Trim().Trim('"').Trim("'")

    if ($key -and [string]::IsNullOrWhiteSpace([Environment]::GetEnvironmentVariable($key, "Process"))) {
      [Environment]::SetEnvironmentVariable($key, $value, "Process")
    }
  }
}

function Resolve-GitHubRepository {
  $remote = (& git remote get-url origin).Trim()
  if ($LASTEXITCODE -ne 0 -or [string]::IsNullOrWhiteSpace($remote)) {
    throw "Could not resolve git remote origin."
  }

  $match = [regex]::Match($remote, "github\.com[:/](?<owner>[^/]+)/(?<repo>[^/.]+)(?:\.git)?/?$")
  if (-not $match.Success) {
    throw "Remote origin is not a GitHub repository URL: $remote"
  }

  return @{
    Owner = $match.Groups["owner"].Value
    Repo = $match.Groups["repo"].Value
  }
}

function Get-DirtyPaths {
  $status = & git status --porcelain
  if (-not $status) {
    return @()
  }

  return @(
    $status | ForEach-Object {
      $line = $_
      if ($line.Length -lt 4) {
        return
      }

      $path = $line.Substring(3).Trim()
      if ($path.Contains(" -> ")) {
        $path = $path.Split(" -> ", 2)[1]
      }

      $path.Replace("\", "/")
    }
  )
}

function Replace-ReadmeValue {
  param(
    [Parameter(Mandatory = $true)][string]$Text,
    [Parameter(Mandatory = $true)][string]$Pattern,
    [Parameter(Mandatory = $true)][scriptblock]$Replacement,
    [Parameter(Mandatory = $true)][string]$Name
  )

  $regex = [regex]::new($Pattern)
  if (-not $regex.IsMatch($Text)) {
    throw "README marker not found: $Name"
  }

  return $regex.Replace($Text, { param($match) & $Replacement $match }, 1)
}

